#include <iostream>
using namespace std;

int main (int argc, char **argv) {

	cout << "size of Double:" << sizeof(double) << endl;
	cout << "size of Integer :"<< sizeof(int) << endl;
	cout << "size of Float :" << sizeof(float) << endl;
	cout << "size of Char:" << sizeof(char) << "; " << "size of  Unsigned Char:" << sizeof(unsigned char) << endl;
	cout << "size of Long:" << sizeof(long) << "; " << "size of Unsigned Long: " << sizeof(unsigned long) << endl;
	cout << "size of Short:" << sizeof(short) << "; " << "size of  Unsigned Short:" << sizeof(unsigned short) << endl;
	system("PAUSE");
	return 0;
	
}